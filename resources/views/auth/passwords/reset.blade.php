<script type="text/javascript">
    function comparar(){
        var nueva = document.getElementById("password").value;
        var repetir = document.getElementById("password-confirm").value;

        if (nueva.length>=8) {
            if(nueva != repetir){
                document.getElementById("falta").innerHTML = "Las contraseñas no coinciden";
                document.getElementById("enviar").disabled=true;
            }else{
                document.getElementById("enviar").disabled=false;
                document.getElementById("falta").innerHTML = "";
            }
        }else{
            document.getElementById("falta").innerHTML = "La contraseña debe tener almenos 8 caracteres";
            document.getElementById("enviar").disabled=true;
        }
    }
</script>
